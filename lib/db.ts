export async function listArticles(search = '', requesterUsername = 'guest'): Promise<Article[]> {
  await ensureDbInitialized();

  const requester = sanitizeUsername(requesterUsername);
  const role = await getRoleForUsername(requester);
  const parsed = parseSearchQuery(search);

  if (useMemory) {
    const visible = memory.articles.filter((article) => isVisible(article, requester, role));
    return searchArticles(visible, parsed.normalized);
  }

  const rows = await sql!`SELECT * FROM articles ORDER BY updated_at DESC`;
  const mapped = rows.map(mapArticle).filter((article) => isVisible(article, requester, role));

  if (parsed.mode === 'empty') return mapped;
  return searchArticles(mapped, parsed.normalized);
}

export async function getArticleBySlug(slug: string, requesterUsername = 'guest'): Promise<Article | null> {
  await ensureDbInitialized();
  if (!isValidSlug(slug)) return null;

  const requester = sanitizeUsername(requesterUsername);
  const role = await getRoleForUsername(requester);

  if (useMemory) {
    const found = memory.articles.find((article) => article.slug === slug) || null;
    if (!found) return null;
    return isVisible(found, requester, role) ? found : null;
  }

  const rows = await sql!`SELECT * FROM articles WHERE slug = ${slug} LIMIT 1`;
  if (rows.length === 0) return null;

  const article = mapArticle(rows[0]);
  return isVisible(article, requester, role) ? article : null;
}

export async function createArticle(payload: ArticleInput): Promise<Article> {
  await ensureDbInitialized();

  const title = sanitizeText(payload.title, MAX_TITLE_LENGTH);
  const content = sanitizeMarkdown(payload.content);
  const tags = normalizeTags(payload.tags);
  const author = sanitizeUsername(payload.authorUsername);
  const role = await getRoleForUsername(author);
  if (!canPublish(role)) {
    throw new Error('Verify your email before creating articles.');
  }
  const status: ArticleStatus = canDirectEdit(role) ? 'published' : 'pending';

  const baseSlug = generateSlug(title);
  let slug = baseSlug;
  let suffix = 1;

  const slugExists = async (value: string) => {
    if (useMemory) {
      return memory.articles.some((article) => article.slug === value);
    }
    const rows = await sql!`SELECT slug FROM articles WHERE slug = ${value} LIMIT 1`;
    return rows.length > 0;
  };

  while (await slugExists(slug)) {
    suffix += 1;
    slug = `${baseSlug}-${suffix}`;
  }

  const id = randomUUID();
  const now = new Date().toISOString();

  if (useMemory) {
    const created: Article = {
      id,
      slug,
      title,
      content,
      tags,
      authorUsername: author,
      status,
      likes: 0,
      views: 0,
      createdAt: now,
      updatedAt: now
    };
    memory.articles.unshift(created);
    return created;
  }

  const rows = await sql!`
    INSERT INTO articles (id, slug, title, content, tags, author_username, status)
    VALUES (${id}, ${slug}, ${title}, ${content}, ${tags}, ${author}, ${status})
    RETURNING *
  `;

  return mapArticle(rows[0]);
}

export async function updateArticle(
  slug: string,
  payload: { title: string; content: string; tags: string[]; editorUsername: string }
): Promise<Article | null> {
  await ensureDbInitialized();
  if (!isValidSlug(slug)) return null;

  const editor = sanitizeUsername(payload.editorUsername);
  const role = await getRoleForUsername(editor);
  if (!canDirectEdit(role)) return null;

  const title = sanitizeText(payload.title, MAX_TITLE_LENGTH);
  const content = sanitizeMarkdown(payload.content);
  const tags = normalizeTags(payload.tags);

  if (useMemory) {
    const index = memory.articles.findIndex((article) => article.slug === slug);
    if (index === -1) return null;

    const updated: Article = {
      ...memory.articles[index],
      title,
      content,
      tags,
      status: 'published',
      updatedAt: new Date().toISOString()
    };
    memory.articles[index] = updated;
    return updated;
  }

  const rows = await sql!`
    UPDATE articles
    SET title = ${title},
        content = ${content},
        tags = ${tags},
        status = ${'published'},
        updated_at = NOW()
    WHERE slug = ${slug}
    RETURNING *
  `;

  if (rows.length === 0) return null;
  return mapArticle(rows[0]);
}

export async function deleteArticle(slug: string, actorUsername: string): Promise<boolean> {
  await ensureDbInitialized();
  if (!isValidSlug(slug)) return false;

  const actor = sanitizeUsername(actorUsername);
  const role = await getRoleForUsername(actor);

  if (useMemory) {
    const before = memory.articles.length;
    memory.articles = memory.articles.filter((article) => {
      if (article.slug !== slug) return true;
      if (role === 'admin') return false;
      return article.authorUsername !== actor;
    });

    memory.likes.forEach((value) => {
      if (value.startsWith(`${slug}::`)) memory.likes.delete(value);
    });

    memory.editRequests = memory.editRequests.filter((request) => request.articleSlug !== slug);
    return memory.articles.length < before;
  }

  await sql!`DELETE FROM article_likes WHERE article_slug = ${slug}`;
  await sql!`DELETE FROM edit_requests WHERE article_slug = ${slug}`;

  if (role === 'admin') {
    const result = await sql!`DELETE FROM articles WHERE slug = ${slug}`;
    return result.count > 0;
  }

  const result = await sql!`DELETE FROM articles WHERE slug = ${slug} AND author_username = ${actor}`;
  return result.count > 0;
}

