export async function upsertProfile(username: string, themeColor: string, roleOverride?: UserRole): Promise<Profile> {
  await ensureDbInitialized();
  const safeUser = sanitizeUsername(username);
  const safeColor = sanitizeThemeColor(themeColor);

  if (useMemory) {
    const index = memory.profiles.findIndex((profile) => profile.username === safeUser);
    const existingRole =
      roleOverride || (safeUser === 'guest' ? 'guest' : index === -1 ? defaultRoleFor(safeUser) : memory.profiles[index].role);

    const profile: Profile = {
      username: safeUser,
      themeColor: safeColor,
      role: existingRole,
      updatedAt: new Date().toISOString()
    };

    if (index === -1) {
      memory.profiles.push(profile);
    } else {
      memory.profiles[index] = profile;
    }
    return profile;
  }

  const existing = await sql!`SELECT * FROM profiles WHERE username = ${safeUser} LIMIT 1`;
  const role = roleOverride || (safeUser === 'guest' ? 'guest' : existing.length > 0 ? sanitizeRole(existing[0].role) : defaultRoleFor(safeUser));

  const rows = await sql!`
    INSERT INTO profiles (username, theme_color, role)
    VALUES (${safeUser}, ${safeColor}, ${role})
    ON CONFLICT (username)
    DO UPDATE SET
      theme_color = EXCLUDED.theme_color,
      role = COALESCE(${roleOverride || null}, profiles.role),
      updated_at = NOW()
    RETURNING *
  `;

  return mapProfile(rows[0]);
}

function buildUsernameFromSeed(seed: string): string {
  const sanitized = sanitizeUsername(seed);
  if (!sanitized || sanitized === 'guest') {
    return `user-${Date.now().toString().slice(-6)}`;
  }
  return sanitized;
}

async function usernameExists(username: string): Promise<boolean> {
  if (useMemory) {
    return memory.users.some((user) => user.username === username);
  }
  const rows = await sql!`SELECT id FROM users WHERE username = ${username} LIMIT 1`;
  return rows.length > 0;
}

async function buildUniqueUsername(seed: string): Promise<string> {
  const base = buildUsernameFromSeed(seed);
  let candidate = base;
  let suffix = 1;

  while (await usernameExists(candidate)) {
    suffix += 1;
    candidate = `${base}-${suffix}`;
  }

  return candidate;
}

