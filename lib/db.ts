export async function issueEmailVerificationToken(
  userId: string
): Promise<{ email: string; username: string; token: string; expiresAt: string } | null> {
  await ensureDbInitialized();
  const user = await getAuthUserById(userId);
  if (!user) return null;

  const token = randomBytes(32).toString('base64url');
  const tokenHash = hashVerificationToken(token);
  const expiresAt = new Date(Date.now() + EMAIL_VERIFICATION_TTL_MS).toISOString();

  if (useMemory) {
    const index = memory.users.findIndex((entry) => entry.id === user.id);
    if (index === -1) return null;

    memory.users[index] = {
      ...memory.users[index],
      verificationTokenHash: tokenHash,
      verificationTokenExpiresAt: expiresAt,
      verificationTokenUsedAt: null,
      updatedAt: new Date().toISOString()
    };
  } else {
    await sql!`
      UPDATE users
      SET verification_token_hash = ${tokenHash},
          verification_token_expires_at = ${expiresAt},
          verification_token_used_at = ${null},
          updated_at = NOW()
      WHERE id = ${user.id}
    `;
  }

  return {
    email: user.email,
    username: user.username,
    token,
    expiresAt
  };
}

export async function verifyEmailVerificationToken(
  token: string
): Promise<{ username: string; email: string; role: UserRole } | null> {
  await ensureDbInitialized();
  const safeToken = sanitizeText(token, 512);
  if (!safeToken) return null;

  const tokenHash = hashVerificationToken(safeToken);
  const now = new Date();
  let user: UserRecord | null = null;

  if (useMemory) {
    user =
      memory.users.find((entry) => {
        if (entry.emailVerified) return false;
        if (!entry.verificationTokenHash || entry.verificationTokenHash !== tokenHash) return false;
        if (entry.verificationTokenUsedAt) return false;
        if (!entry.verificationTokenExpiresAt) return false;
        return new Date(entry.verificationTokenExpiresAt).getTime() > now.getTime();
      }) || null;

    if (!user) return null;

    const index = memory.users.findIndex((entry) => entry.id === user!.id);
    const verifiedAt = now.toISOString();
    memory.users[index] = {
      ...memory.users[index],
      emailVerified: true,
      verificationTokenHash: '',
      verificationTokenExpiresAt: null,
      verificationTokenUsedAt: verifiedAt,
      updatedAt: verifiedAt
    };
    user = memory.users[index];
  } else {
    const rows = await sql!`
      SELECT * FROM users
      WHERE verification_token_hash = ${tokenHash}
        AND email_verified = FALSE
        AND verification_token_used_at IS NULL
        AND verification_token_expires_at IS NOT NULL
        AND verification_token_expires_at > NOW()
      LIMIT 1
    `;
    if (rows.length === 0) return null;

    const verifiedRows = await sql!`
      UPDATE users
      SET email_verified = ${true},
          verification_token_hash = ${''},
          verification_token_expires_at = ${null},
          verification_token_used_at = NOW(),
          updated_at = NOW()
      WHERE id = ${rows[0].id}
      RETURNING *
    `;
    user = verifiedRows.length > 0 ? mapUser(verifiedRows[0]) : null;
  }

  if (!user) return null;

  const currentProfile = await getProfile(user.username);
  const nextRole = currentProfile?.role === 'editor' || currentProfile?.role === 'admin' ? currentProfile.role : 'user';
  const profile = await upsertProfile(user.username, currentProfile?.themeColor || '#2563eb', nextRole);

  return {
    username: user.username,
    email: user.email,
    role: profile.role
  };
}

export async function createAuthUser(payload: {
  username: string;
  email: string;
  passwordHash?: string;
  provider?: string;
  providerAccountId?: string;
  emailVerified?: boolean;
  role?: UserRole;
}): Promise<{ id: string; username: string; email: string; role: UserRole } | null> {
  await ensureDbInitialized();

  const safeEmail = sanitizeEmail(payload.email);
  if (!safeEmail) return null;

  const existingByEmail = await getAuthUserByEmail(safeEmail);
  if (existingByEmail) return null;

  const username = await buildUniqueUsername(payload.username || safeEmail.split('@')[0] || 'user');
  const id = randomUUID();
  const now = new Date().toISOString();
  const passwordHash = sanitizeText(payload.passwordHash || '', 255);
  const provider = sanitizeText(payload.provider || 'credentials', 32) || 'credentials';
  const providerAccountId = sanitizeText(payload.providerAccountId || '', 120);
  const initialRole = sanitizeRole(payload.role || (payload.emailVerified ? 'user' : 'pending'));
  const emailVerified = Boolean(payload.emailVerified);

  if (useMemory) {
    memory.users.push({
      id,
      username,
      email: safeEmail,
      passwordHash,
      provider,
      providerAccountId,
      emailVerified,
      verificationTokenHash: '',
      verificationTokenExpiresAt: null,
      verificationTokenUsedAt: null,
      createdAt: now,
      updatedAt: now
    });
  } else {
    await sql!`
      INSERT INTO users (
        id,
        username,
        email,
        password_hash,
        provider,
        provider_account_id,
        email_verified,
        verification_token_hash,
        verification_token_expires_at,
        verification_token_used_at,
        created_at,
        updated_at
      )
      VALUES (
        ${id},
        ${username},
        ${safeEmail},
        ${passwordHash},
        ${provider},
        ${providerAccountId},
        ${emailVerified},
        ${''},
        ${null},
        ${null},
        ${now},
        ${now}
      )
    `;
  }

  const profile = await upsertProfile(username, '#2563eb', initialRole);
  return { id, username, email: safeEmail, role: profile.role };
}

