export async function submitEditRequest(payload: {
  slug: string;
  requesterUsername: string;
  intent: string;
  proposedTitle?: string;
  proposedContent?: string;
  proposedTags?: string[];
}): Promise<EditRequest | null> {
  await ensureDbInitialized();

  const slug = sanitizeText(payload.slug, 180);
  if (!isValidSlug(slug)) return null;

  const requester = sanitizeUsername(payload.requesterUsername);
  if (!requester || requester === 'guest') return null;

  const role = await getRoleForUsername(requester);
  if (role === 'pending') return null;
  if (canDirectEdit(role)) return null;
  if (!canRequestEdits(role)) return null;

  const article = await getArticleBySlug(slug, requester);
  if (!article) return null;

  const intent = sanitizeText(payload.intent, 400);
  const proposedTitle = sanitizeText(payload.proposedTitle || '', MAX_TITLE_LENGTH);
  const proposedContent = sanitizeMarkdown(payload.proposedContent || '');
  const proposedTags = normalizeTags(payload.proposedTags || []);
  const hasProposedChanges = Boolean(proposedTitle || proposedContent.trim() || proposedTags.length > 0);

  if (intent.length < 8 && !hasProposedChanges) {
    return null;
  }

  const now = new Date().toISOString();
  const request: EditRequest = {
    id: randomUUID(),
    articleSlug: slug,
    requesterUsername: requester,
    intent,
    proposedTitle,
    proposedContent,
    proposedTags,
    status: 'pending',
    reviewedBy: '',
    createdAt: now,
    updatedAt: now
  };

  if (useMemory) {
    memory.editRequests.unshift(request);
    return request;
  }

  const rows = await sql!`
    INSERT INTO edit_requests (id, article_slug, requester_username, intent, proposed_title, proposed_content, proposed_tags, status, reviewed_by)
    VALUES (${request.id}, ${slug}, ${requester}, ${intent}, ${proposedTitle}, ${proposedContent}, ${proposedTags}, ${'pending'}, ${''})
    RETURNING *
  `;

  return mapEditRequest(rows[0]);
}

